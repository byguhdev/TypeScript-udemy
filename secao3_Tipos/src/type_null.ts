// Type null

/*
    1 - "null" é um valor atribuído explicitamente por um programador para indicar a ausência intencional de um objeto ou valor.

    2 - "undefined", por outro lado, é um valor atribuído automaticamente a uma variável que não possui um valor atribuído ou a uma propriedade que não foi definida em um objeto.
*/

// Usando o null
let nome: string | null;
nome = "Dr NULL";
nome = null;
console.log(nome);

// Usando o undefined
// undefined -> padrão de toda variavel quando não se sabe sua tipagem.
let indefinido;
console.log(indefinido);
